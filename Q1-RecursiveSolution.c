#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#define ToSort( m,n ) \
    if( (m) < (n) ){ \
        int t = (m); \
        (m) = (n); \
        (n) = t; \
    }
void Q1( int m, int n);
void Q2( int m, int n);

/********************************************************************************************
********************************************************************************************/
int main() {

    Q1(1,1);
    Q1(5,5);
    Q1(5,26);
    Q1(26,5);
    Q1(5,1);
    Q1(1,5);

    return 0;
}

/********************************************************************************************
********************************************************************************************/
void Q1( int m, int n)
{
    if( m == n ){
        printf( "%dx%d\n",m,m );
        return;
    }

    ToSort(m,n);
    printf( "%dx%d",n,n );

    Q2(m-n,n);

    printf("\n");

}

/********************************************************************************************
********************************************************************************************/
void Q2( int m, int n)
{
    if( m<=0 || n<= 0 ) return;

    ToSort(m,n);
    printf( ", %dx%d",n,n );

    Q2(m-n,n);
}
