#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#define ToSort( m,n ) \
    if( (m) < (n) ){ \
        int t = (m); \
        (m) = (n); \
        (n) = t; \
    }

void Q1( int m, int n);

/********************************************************************************************
********************************************************************************************/
int main() {

    Q1(1,1);
    Q1(5,5);
    Q1(5,26);
    Q1(26,5);
    Q1(5,1);
    Q1(1,5);

    return 0;
}

/********************************************************************************************
********************************************************************************************/
void Q1( int m, int n)
{
    // asuming m>0,n>0

    ToSort(m,n);
    printf( "%dx%d",n,n );

    m -= n;
    while( m > 1 ){
        printf( ", %dx%d",n,n );
        m -= n;
        ToSort(m,n);
    }

    if( 1 == m && n == 1 ) printf( ", 1x1" );

    printf( "\n" );
}
